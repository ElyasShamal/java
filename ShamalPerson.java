public class ShamalPerson {

	public static void main(String[] args) {
		
		System.out.print("My name is Elyas Shamal \n"); // \n escape sequence is used to move the output to the next line within a single System.out.print().
		System.out.println("I live in Durham, NC "); // System.out.println() automatically moves to the next line after printing.
		System.out.print("My phone number is 111-111-1111 \n");
		System.out.println("My major is Information Technology Software Development");
		System.out.print("My dream profession is to be a Software Engineer");		
	}

}
